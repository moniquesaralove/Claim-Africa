const PLAYER_COLORS = {
  1: { fill: "#C9682E", label: "Player 1" },
  2: { fill: "#2E6E63", label: "Player 2" },
};

const UNCLAIMED_FILL = "#D8CDB0";
const UNCLAIMED_STROKE = "#A89B7C";

function pickRandomTarget(claims) {
  const remaining = Object.keys(COUNTRY_PATHS).filter((n) => !claims[n]);
  if (remaining.length === 0) return null;
  return remaining[Math.floor(Math.random() * remaining.length)];
}

export default function AfricaGame() {
  const [screen, setScreen] = useState("nameEntry"); // nameEntry, waitingForPlayer2, game
  const [player1Name, setPlayer1Name] = useState("");
  const [player2Name, setPlayer2Name] = useState("");
  const [gameCode, setGameCode] = useState("");
  const [gameId, setGameId] = useState(null);
  const [myPlayerNum, setMyPlayerNum] = useState(null);
  
  const [game, setGame] = useState(null);
  const [loading, setLoading] = useState(false);
  const [copyLabel, setCopyLabel] = useState("Copy invite link");

  // Start game as Player 1
  const startGame = async () => {
    if (!player1Name.trim()) return;
    setLoading(true);
    
    const newGameId = Math.random().toString(36).slice(2, 10);
    const initialClaims = {};
    const initialTarget = pickRandomTarget(initialClaims);

    try {
      const { error } = await supabase
        .from("games")
        .insert([
          {
            id: newGameId,
            player1_name: player1Name,
            current_turn: 1,
            target_country: initialTarget,
          },
        ]);

      if (error) throw error;

      setGameCode(newGameId);
      setGameId(newGameId);
      setMyPlayerNum(1);
      setGame({
        id: newGameId,
        player1_name: player1Name,
        player2_name: null,
        current_turn: 1,
        target_country: initialTarget,
      });
      setScreen("waitingForPlayer2");
    } catch (err) {
      console.error("Error creating game:", err);
    }
    setLoading(false);
  };

  // Join game as Player 2
  const joinGame = async () => {
    if (!player2Name.trim() || !gameCode.trim()) return;
    setLoading(true);

    try {
      const { data, error } = await supabase
        .from("games")
        .select("*")
        .eq("id", gameCode)
        .single();

      if (error || !data) {
        alert("Game not found");
        setLoading(false);
        return;
      }

      const { error: updateError } = await supabase
        .from("games")
        .update({ player2_name: player2Name })
        .eq("id", gameCode);

      if (updateError) throw updateError;

      setGameId(gameCode);
      setMyPlayerNum(2);
      setGame(data);
      setScreen("game");
    } catch (err) {
      console.error("Error joining game:", err);
    }
    setLoading(false);
  };

  // Listen to game updates
  useEffect(() => {
    if (!gameId) return;

    const subscription = supabase
      .from("games")
      .on("*", (payload) => {
        setGame(payload.new);
      })
      .subscribe();

    return () => {
      subscription.unsubscribe();
    };
  }, [gameId]);

  const handleCopyLink = async () => {
    try {
      await navigator.clipboard.writeText(
        `${window.location.origin}?join=${gameCode}`
      );
      setCopyLabel("Link copied!");
    } catch (e) {
      setCopyLabel("Copy failed");
    }
    setTimeout(() => setCopyLabel("Copy invite link"), 2000);
  };

  if (screen === "nameEntry") {
    const queryParams = new URLSearchParams(window.location.search);
    const joinCode = queryParams.get("join");

    return (
      <div style={styles.page}>
        <h1 style={styles.title}>Claim Africa</h1>
        {!joinCode ? (
          <div style={styles.card}>
            <h2>Create Game</h2>
            <input
              type="text"
              placeholder="Your name"
              value={player1Name}
              onChange={(e) => setPlayer1Name(e.target.value)}
              style={styles.input}
            />
            <button onClick={startGame} disabled={loading} style={styles.btn}>
              {loading ? "Creating..." : "Create Game"}
            </button>
          </div>
        ) : (
          <div style={styles.card}>
            <h2>Join Game</h2>
            <input
              type="text"
              placeholder="Your name"
              value={player2Name}
              onChange={(e) => setPlayer2Name(e.target.value)}
              style={styles.input}
            />
            <button
              onClick={() => joinGame()}
              disabled={loading}
              style={styles.btn}
            >
              {loading ? "Joining..." : "Join Game"}
            </button>
          </div>
        )}
      </div>
    );
  }

  if (screen === "waitingForPlayer2") {
    return (
      <div style={styles.page}>
        <h1 style={styles.title}>Waiting for Player 2</h1>
        <p>Share this code with your friend:</p>
        <div style={styles.codeBox}>{gameCode}</div>
        <button onClick={handleCopyLink} style={styles.btn}>
          {copyLabel}
        </button>
        {game?.player2_name && (
          <>
            <p>{game.player2_name} has joined!</p>
            <button
              onClick={() => setScreen("game")}
              style={styles.btn}
            >
              Start Game
            </button>
          </>
        )}
      </div>
    );
  }

  if (screen === "game" && game) {
    return (
      <GameBoard game={game} myPlayerNum={myPlayerNum} gameId={gameId} />
    );
  }

  return <div style={styles.page}>Loading...</div>;
}

function GameBoard({ game, myPlayerNum, gameId }) {
  const [claims, setClaims] = useState({});
  const [hovered, setHovered] = useState(null);
  const [missClick, setMissClick] = useState(null);

  useEffect(() => {
    const fetchClaims = async () => {
      const { data, error } = await supabase
        .from("claims")
        .select("*")
        .eq("game_id", gameId);

      if (!error && data) {
        const claimsMap = {};
        data.forEach((claim) => {
          claimsMap[claim.country_name] = claim.claimed_by;
        });
        setClaims(claimsMap);
      }
    };

    fetchClaims();

    const subscription = supabase
      .from("claims")
      .on("*", (payload) => {
        setClaims((prev) => ({
          ...prev,
          [payload.new.country_name]: payload.new.claimed_by,
        }));
      })
      .subscribe();

    return () => {
      subscription.unsubscribe();
    };
  }, [gameId]);

  const handlePick = async (name) => {
    if (claims[name] || game.current_turn !== myPlayerNum) return;
    if (name !== game.target_country) {
      // Wrong click
      setMissClick({ name });
      setTimeout(() => setMissClick(null), 700);
      // Switch turn
      const nextTurn = game.current_turn === 1 ? 2 : 1;
      await supabase
        .from("games")
        .update({ current_turn: nextTurn })
        .eq("id", gameId);
      return;
    }

    // Correct click
    await supabase.from("claims").insert([
      { game_id: gameId, country_name: name, claimed_by: myPlayerNum },
    ]);

    const nextTurn = myPlayerNum === 1 ? 2 : 1;
    const newClaims = { ...claims, [name]: myPlayerNum };
    const newTarget = pickRandomTarget(newClaims);

    await supabase
      .from("games")
      .update({ current_turn: nextTurn, target_country: newTarget })
      .eq("id", gameId);
  };

  const p1Count = Object.values(claims).filter((v) => v === 1).length;
  const p2Count = Object.values(claims).filter((v) => v === 2).length;
  const total = Object.keys(COUNTRY_PATHS).length;
  const remaining = total - Object.keys(claims).length;

  const isMyTurn = game.current_turn === myPlayerNum;
  const canPlay = isMyTurn && remaining > 0;

  return (
    <div style={styles.page}>
      <h1 style={styles.title}>{game.target_country}</h1>
      <div style={styles.info}>
        <div>
          {game.player1_name} ({p1Count})
        </div>
        <div>
          {game.current_turn === 1 ? game.player1_name : game.player2_name}'s turn
        </div>
        <div>
          {game.player2_name} ({p2Count})
        </div>
      </div>

      {!isMyTurn && <p>Waiting for {game.current_turn === 1 ? game.player1_name : game.player2_name}...</p>}

      <div style={styles.mapWrap}>
        <svg viewBox={VIEW_BOX} style={styles.svg}>
          {Object.entries(COUNTRY_PATHS).map(([name, d]) => {
            const owner = claims[name];
            const isMiss = missClick && missClick.name === name;
            let fill = UNCLAIMED_FILL;
            if (owner) fill = PLAYER_COLORS[owner].fill;
            if (isMiss) fill = "#B5483A";
            return (
              <path
                key={name}
                d={d}
                fill={fill}
                stroke={owner ? "#2A2620" : UNCLAIMED_STROKE}
                strokeWidth={isMiss ? 2.4 : 1}
                style={{
                  cursor: canPlay && !owner ? "pointer" : "default",
                  transition: "fill 0.2s ease",
                  opacity: canPlay && !owner ? 1 : 0.8,
                }}
                onMouseEnter={() => !owner && setHovered(name)}
                onMouseLeave={() => setHovered(null)}
                onClick={() => canPlay && handlePick(name)}
              />
            );
          })}
        </svg>
      </div>

      <div style={styles.scoreRow}>
        <span>{remaining}/{total} left</span>
      </div>
    </div>
  );
}

